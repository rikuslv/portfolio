// Current language state
const STORAGE_KEY = 'portfolio-language';

function getInitialLanguage() {
    const storedLanguage = localStorage.getItem(STORAGE_KEY);
    return storedLanguage === 'lv' || storedLanguage === 'en' ? storedLanguage : 'en';
}

let currentLanguage = getInitialLanguage();

function translate(key) {
    return translite[currentLanguage][key] || key;
}

// Toggle language function - add your translation logic here
function toggleLanguage() {
    currentLanguage = currentLanguage === 'en' ? 'lv' : 'en';
    localStorage.setItem(STORAGE_KEY, currentLanguage);
    
    if (typeof renderPortfolio === 'function') {
        renderPortfolio();
    }

    // Update page content based on currentLanguage
    updatePageContent();
    
    console.log('Language switched to:', currentLanguage);
}

// Update page content based on selected language
function updatePageContent() {
    // Translate all elements with data-i18 attribute
    document.querySelectorAll('[data-i18]').forEach(element => {
        const key = element.getAttribute('data-i18');
        const val = translite[currentLanguage] && translite[currentLanguage][key];
        if (val !== undefined) {
            if (typeof val === 'string' && val.indexOf('<') !== -1) {
                element.innerHTML = val;
            } else {
                element.textContent = val;
            }
        }
    });

    document.querySelectorAll('[data-i18-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18-placeholder');
        if (translite[currentLanguage][key]) {
            element.placeholder = translite[currentLanguage][key];
        }
    });
    
    // Update button text
    const button = document.querySelector('.translate-link');
    if (button) {
        button.textContent = currentLanguage === 'en' ? 'Latvian' : 'English';
    }

    document.documentElement.lang = currentLanguage === 'lv' ? 'lv' : 'en';

    // Update the browser title if translated
    if (translite[currentLanguage].title) {
        document.title = translite[currentLanguage].title;
    }
}

updatePageContent();
